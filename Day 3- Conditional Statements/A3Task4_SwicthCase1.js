//Find the largest of three numbers using nested if-else statements.
let dayNumber = 3;
let dayName;
switch (dayNumber) {
  case 1:
    dayName = "Sunday";
    break;
  case 2:
    dayName = "Monday";
    break;
  case 3:
    dayName = "Tuesday";
    break;
  case 4:
    dayName = "Wednesday";
    break;
  case 5:
    dayName = "Thurday";
    break;
  case 6:
    dayName = "Friday";
    break;
  case 7:
    "Saturday";
    break;
  default:
    dayName = "Invalid Day Number";
}

console.log(dayName);

//Output: Tuesday
