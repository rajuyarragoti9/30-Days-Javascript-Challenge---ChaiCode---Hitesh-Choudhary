//: Use the ternary operator to determine if a number is positive or negative and log the result.

let number = 7;
let result = number >= 0 ? "Positive" : "Negative";

console.log(result); 

// Output: Positive


let number1 = -7;
let result2 = number1 >= 0 ? "Positive" : "Negative";

console.log(result2); 

// Output: Negative